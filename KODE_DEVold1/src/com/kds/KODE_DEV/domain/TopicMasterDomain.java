package com.kds.KODE_DEV.domain;

public class TopicMasterDomain {
	private String TopDetails;
	private String TopID;
	private String SubID;
	
	public String getTopicDetails() {
		return TopDetails;
	}
	public void setTopicDetails(String TopDetails) {
		this.TopDetails = TopDetails;
	}
	public String getTopicID() {
		return TopID;
	}
	public void setTopicID(String TopID) {
		this.TopID = TopID;
	}
	public String getSubjectID() {
		return SubID;
	}
	public void setSubjectID(String SubID) {
		this.SubID = SubID;
	}
}